package reto10;
/**
 * La clase CEO extiende de la clase Manager e implementa los atributos necesarios para representar a un CEO en una empresa.
 */
public class CEO extends Manager {

    /**
     * La variable que representa el número de la empresa a la que pertenece el CEO.
     */
    public int empresa;

    /**
     * La variable que representa el valor total de la empresa a la que pertenece el CEO.
     */
    public int valorEmpresa;

    /**
     * La variable que representa el valor de la fracción de la empresa que posee el CEO.
     */
    public int valorFraccionEmpresa;
}
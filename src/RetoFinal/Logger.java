package RetoFinal;
/**
 Una clase que su función es imprimir logs en la consola.
 */
public class Logger {
    public Logger() {
    }

    /**
     * Método estático que imprime el mensaje dado en la consola.
     * @param var0 el mensaje que se imprimirá en la consola.
     */
    static void log(String var0) {
        System.out.println(var0);
    }
}
